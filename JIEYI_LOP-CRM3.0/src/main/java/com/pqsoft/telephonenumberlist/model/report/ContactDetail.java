package com.pqsoft.telephonenumberlist.model.report;

import com.alibaba.fastjson.annotation.JSONField;

import java.math.BigDecimal;
import java.util.Date;

/**
 * TODO
 *
 * @author 钟林俊
 * @version V1.0 2016-06-20 13:58
 */
public class ContactDetail {

    @JSONField(name = "sms_cnt")
    private Integer smsCnt;
    @JSONField(name = "phone_num")
    private String phoneNum;
    @JSONField(name = "phone_num_loc")
    private String phoneNumLoc;
    @JSONField(name = "trans_start", format = "yyyy-MM-dd HH:mm:ss")
    private Date transStart;
    @JSONField(name = "call_in_cnt")
    private Integer callInCnt;
    @JSONField(name = "call_cnt")
    private Integer callCnt;
    @JSONField(name = "call_len")
    private BigDecimal callLen;
    @JSONField(name = "call_out_cnt")
    private Integer callOutCnt;
    @JSONField(name = "trans_end", format = "yyyy-MM-dd HH:mm:ss")
    private Date transEnd;

    public Integer getSmsCnt() {
        return smsCnt;
    }

    public void setSmsCnt(Integer smsCnt) {
        this.smsCnt = smsCnt;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public String getPhoneNumLoc() {
        return phoneNumLoc;
    }

    public void setPhoneNumLoc(String phoneNumLoc) {
        this.phoneNumLoc = phoneNumLoc;
    }

    public Date getTransStart() {
        return transStart;
    }

    public void setTransStart(Date transStart) {
        this.transStart = transStart;
    }

    public Integer getCallInCnt() {
        return callInCnt;
    }

    public void setCallInCnt(Integer callInCnt) {
        this.callInCnt = callInCnt;
    }

    public Integer getCallCnt() {
        return callCnt;
    }

    public void setCallCnt(Integer callCnt) {
        this.callCnt = callCnt;
    }

    public BigDecimal getCallLen() {
        return callLen;
    }

    public void setCallLen(BigDecimal callLen) {
        this.callLen = callLen;
    }

    public Integer getCallOutCnt() {
        return callOutCnt;
    }

    public void setCallOutCnt(Integer callOutCnt) {
        this.callOutCnt = callOutCnt;
    }

    public Date getTransEnd() {
        return transEnd;
    }

    public void setTransEnd(Date transEnd) {
        this.transEnd = transEnd;
    }
}
