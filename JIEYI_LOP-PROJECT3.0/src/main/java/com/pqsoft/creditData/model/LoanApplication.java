package com.pqsoft.creditData.model;


import java.math.BigDecimal;

/**
 * 
 * 贷款申请信息
 * 
 * @className LoanApplication
 * @author 刘丽
 * @version V1.0 2016年4月21日 下午3:53:29
 */
public class LoanApplication extends BaseModel {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    private String applicationNo;
    private String applicationType;
    private BigDecimal applicationAmount;
    private String applicationMonthQuantity;
    private String applicationTime;
    private String applicationStatus;
    private Identity identity;

    /**
     * 得到贷款申请号
     * 
     */
    public String getApplicationNo() {
        return applicationNo;
    }

    /**
     * 设置贷款申请号
     * 
     */
    public void setApplicationNo(String applicationNo) {
        this.applicationNo = applicationNo;
    }

    /**
     * 得到贷款申请类型
     * 
     */
    public String getApplicationType() {
        return applicationType;
    }

    /**
     * 设置贷款申请类型
     * 
     */
    public void setApplicationType(String applicationType) {
        this.applicationType = applicationType;
    }

    /**
     * 得到贷款申请金额
     * 
     */
    public BigDecimal getApplicationAmount() {
        return applicationAmount;
    }

    /**
     * 设置贷款申请金额
     * 
     */
    public void setApplicationAmount(BigDecimal applicationAmount) {
        this.applicationAmount = applicationAmount;
    }

    /**
     * 得到贷款申请月数
     * 
     */
    public String getApplicationMonthQuantity() {
        return applicationMonthQuantity;
    }

    /**
     * 设置贷款申请月数
     * 
     */
    public void setApplicationMonthQuantity(String applicationMonthQuantity) {
        this.applicationMonthQuantity = applicationMonthQuantity;
    }

    /**
     * 得到贷款申请时间
     * 
     */
    public String getApplicationTime() {
        return applicationTime;
    }

    /**
     * 设置贷款申请时间
     * 
     */
    public void setApplicationTime(String applicationTime) {
        this.applicationTime = applicationTime;
    }

    /**
     * 得到贷款申请状态
     * 
     */
    public String getApplicationStatus() {
        return applicationStatus;
    }

    /**
     * 设置贷款申请状态
     * 
     */
    public void setApplicationStatus(String applicationStatus) {
        this.applicationStatus = applicationStatus;
    }

    public Identity getIdentity() {
        return identity;
    }

    public void setIdentity(Identity identity) {
        this.identity = identity;
    }

    @Override
    public String toString() {
        return "Application{" +
                "applicationNo='" + applicationNo + '\'' +
                ", applicationType='" + applicationType + '\'' +
                ", applicationAmount=" + applicationAmount +
                ", applicationMonthQuantity='" + applicationMonthQuantity + '\'' +
                ", applicationTime='" + applicationTime + '\'' +
                ", applicationStatus='" + applicationStatus + '\'' +
                ", identity=" + identity +
                "} " + super.toString();
    }
}
